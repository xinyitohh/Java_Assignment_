    public Hall_Availability_Operation(int position, EditListener listener){
        initComponents();
        setDefaultCloseOperation(javax.swing.WindowConstants.DISPOSE_ON_CLOSE);        
        this.position = position;
        this.listener = listener;

        try{
            schedule = FileOperation.ReadSchedule("Schedule.txt");
            Schedule scheduleData = schedule.get(position);
            cboScheduleType.setSelectedItem(scheduleData.getSchedule_Type());
            txtHallName.setText(scheduleData.getHall_Name());
            SimpleDateFormat dformat = new SimpleDateFormat("dd-MM-yyyy");
            try{
                dteStart.setDate(dformat.parse(scheduleData.getStart_Date()));
                dteEnd.setDate(dformat.parse(scheduleData.getEnd_Date()));
            }catch (ParseException px){System.out.println(px);}
            String[] timePart1 = scheduleData.getStart_Time().replace("AM", "").replace("PM", "").split(":");
            String[] timePart2 = scheduleData.getEnd_Time().replace("AM", "").replace("PM", "").split(":");
            
            cboStartHour.setSelectedItem(timePart1[0]);
            cboEndHour.setSelectedItem(timePart2[0]);
            
            spnStartMinutes.setValue(Integer.parseInt(timePart1[1]));
            spnEndMinutes.setValue(Integer.parseInt(timePart2[1]));
            String remark;

            if (( remark= scheduleData.getRemarks()).equals("null")){
                txtfRemarks.setText(null);
            }
            else{
                txtfRemarks.setText(remark);
            }
            
        } catch(IOException ex){System.out.println(ex);}
    }

