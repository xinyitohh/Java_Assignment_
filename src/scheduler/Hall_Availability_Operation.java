    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String type = cboScheduleType.getSelectedItem().toString();
        
        String hallName = txtHallName.getText();
        
        String start_hour = cboStartHour.getSelectedItem().toString();
        String start_minutes = String.format("%02d",spnStartMinutes.getValue());
        String time1 = txtAmPm1.getText();
        
        String end_hour = cboEndHour.getSelectedItem().toString();
        String end_minutes = String.format("%02d", spnEndMinutes.getValue());
        String time2 = txtAmPm2.getText();
        
        String start_time = start_hour + ":" + start_minutes + time1;
        String end_time = end_hour + ":" + end_minutes + time2;

        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        String start_date = dateFormat.format(dteStart.getDate());
        String end_date = dateFormat.format(dteEnd.getDate());
        String remark = txtfRemarks.getText();
        if (remark.equals("")){remark = null;}
        

        validater = new Schedule(type,hallName,start_date,start_time,end_date,end_time,remark,position);
        
        if (validater.ValidateData()){
            Schedule editedRow = schedule.get(position);
            
            editedRow.setSchedule_Type(type);
            editedRow.setHall_Name(hallName);
            editedRow.setStart_Date(start_date);
            editedRow.setStart_Time(start_time);
            editedRow.setEnd_Date(end_date);
            editedRow.setEnd_Time(end_time);
            editedRow.setRemarks(remark);
            
            FileOperation.FileWriting("Schedule.txt", schedule);
            JOptionPane.showMessageDialog(null, "Data edited successfully");
            this.dispose();
            windowClosing();
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int confirm = JOptionPane.showConfirmDialog(null, "You are going to delete the data. Do you confirm?", "Confirmation", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION){        
            FileOperation.DeleteFile("Schedule.txt");
            schedule.remove(schedule.get(position));

            for (Schedule sc: schedule){
                Schedule s = new Schedule(sc.getSchedule_Type(),sc.getHall_Name(),sc.getStart_Date(),sc.getStart_Time(),sc.getEnd_Date(),sc.getEnd_Time(),sc.getRemarks());
                s.WriteSchedule();
            }
            JOptionPane.showMessageDialog(null, "The data has been deleted successfully");
            this.dispose();
            windowClosing();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

