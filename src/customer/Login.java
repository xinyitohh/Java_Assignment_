    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String id = txtId.getText();
        String password = new String(txtPassword.getPassword());

        Customer customer = authenticateUser(id, password); 

        if (customer != null) {
            // set customer in session (loggedin user)
            Session.getInstance().setCustomer(customer);

            CustomerDashboard dashboard = new CustomerDashboard(customer); 
            dashboard.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Invalid ID or Password");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

   private Customer authenticateUser(String id, String password) {
    try (BufferedReader reader = new BufferedReader(new FileReader("customer.txt"))) {
        String line;
        while ((line = reader.readLine()) != null) {
            String[] data = line.split(";");
            if (data[0].equals(id) && data[6].equals(password)) {
                // create and return a Customer object if credentials match
                return new Customer(
                    data[0],   // userId
                    data[1],   // name
                    data[2],   // email
                    data[3],   // phone
                    data[4],   // gender
                    data[5],   // picture
                    data[6],   // password
                    Double.parseDouble(data[7]) // balance
                );
            }
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
    return null; // user not found or password incorrect
}
    

